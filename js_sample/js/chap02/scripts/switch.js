var rank = 'B';
switch(rank) {
  case 'A' :
    console.log('Aランクです。');
    break;
  case 'B' :
    console.log('Bランクです。');
    break;
  case 'C' :
    console.log('Cランクです。');
    break;
  default :
    console.log('ランク外です。');
    break;
}
